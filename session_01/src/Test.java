
public class Test {
	
	public static void main(String[] args) {
		
		int no = 10;
		System.out.println("no = "+no);
		
		float no1 =(float)5/2;
		System.out.println("no1 = "+no1);
		
		int no2 = 5%2;
		System.out.println("no2 = "+no2);
		
		int a = no++;
		System.out.println("no = "+no+" a = "+a);
		
		int b = ++no;
		System.out.println("no = "+no+" b = "+b);
		
		//System.out.println(10/0);//ArithmeticException
		
		System.out.println("10/0.0 = "+10.0/0.0);//Infinity
		
		System.out.println("10/0.0 = "+0.0/0.0);//Nan
		
		
		
	}

}


